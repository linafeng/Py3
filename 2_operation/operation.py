'''
基本运算符
'''
#加法运算
first_number = 5
second_number = 6
result = first_number+second_number
print(result)

#减法运算
result=first_number-second_number
print(result)

#乘法运算
result=first_number*second_number
print(result)

#除法运算
result=first_number/second_number
print(result)
print(type(result))


'''
算数运算符
'''
#取模运算也即是取余数
i=10
j=3
z=i%j
print(z)
#幂运算
z=i**j
print(z)

#取整除运算
z=i//j
print(z)

'''
赋值运算符
'''
#加等于
a=1
